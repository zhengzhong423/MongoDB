package MangoDB.MANGO;

public class JavaBean {
	String Url;
	String Status;
	public JavaBean()
	{
		
	}
	
	public void setUrl(String s)
	{
		this.Url=s;
	}
	public String getUrl()
	{
		return this.Url;
	}
	
	
	public void setStatus(String s)
	{
		this.Status=s;
	}
	public String getStatus()
	{
		return this.Status;
	}
}
